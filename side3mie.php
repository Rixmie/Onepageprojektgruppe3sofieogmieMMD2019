        <h2 id="ophav">Ophavsret</h2>
        <p id=ophav1> Ophavsret i Danmark er en lovgivning som er med til at sikre mod tyveri af både digitale og fysiske medier det vil sige at man ikke må kopiere et billede, en tekst et design eller en video uden tilladelse fra ophavsretshaveren.(kilde: http://www.jura-guide.dk/ophavsret)  <br>
        Det som der indebærer at man kan få ophavsret på noget, er at det bære præg af originalitet samt det skal være skabt fra bunden af, af skaberen(kilde: http://www.jura-guide.dk/ophavsret  krav ved ophavsret).<br>
        Billeder som er taget med et kamera, en tekst som er skrevet fra bunden, vil altid være bundet af ophavsretten også selv om skaberen ikke gør opmærksom på det ved f.eks. at sætte Copyright-symbolet på deres kreationer eller på hjemmesiden de ligger(kilde: http://www.jura-guide.dk/ophavsret  krav ved ophavsret).
        Selvom der er generelle regler hvad angår ophavsret, så er der med den udvikling som der er sket digitalt over de seneste årtier. Så der er udformet regler for ophavsret for socialemedier </p>
        <p id="ophav3"> ”Når du bruger de sociale medier som f.eks. Facebook og Twitter, skal du derfor være opmærksom på, om du overholder reglerne om f.eks. upload og linking. Du må f.eks. ikke offentligt tilgængeliggøre musik eller billeder, som andre har rettighederne til, på din Facebookprofil uden tilladelse fra rettighedshaveren.<br> 
        For det andet betyder det også, at du skal overholde reglerne om download og streaming, når du bruger det materiale, som andre deler. <br>
        Du må f.eks. ikke downloade andres musik eller billeder, som de har lagt ulovligt ud på deres profil.<br>
        Du skal være opmærksom på, at mange af de sociale medier kræver, at du giver udbyderen, f.eks. Facebook, licens til at måtte bruge alt det materiale, som du lægger ud på din profil. <br>
        Du accepterer typisk denne betingelse, når du godkender brugervilkårene for det sociale medie i forbindelse med oprettelse af din profil. <br>
        Når du først har godkendt betingelserne, kan du f.eks. ikke almindeligvis modsætte dig, at udbyderen bruger dine billeder til reklame eller sælger billederne til andre i overensstemmelse med vilkårene.<br>
        Du skal desuden være opmærksom på, at de sociale medier sjældent har hjemme i Danmark. <br>
        Det fremgår ofte af brugervilkårene og kan få betydning for eventuelle retstvister mellem dig og det sociale medie”(kilde: https://kum.dk/kulturpolitik/ophavsret/ophavsret-paa-internettet/#c81807).
        </p> 
